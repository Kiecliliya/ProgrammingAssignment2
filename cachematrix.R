## Create a function will create a matrix that can cache its inverse and a
## second function that computes the inverse from the matrix created by the 
## first function.

## Creates a matrix that can cache its inverse.

makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) i <<- inverse
  getinverse <- function() i
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


## Solves for the inverse of the matrix returned by the makeCacheMatrix 
## function, returning the cached inverse matrix if the inverse has already 
## been calculated and the matrix not changed.

cacheSolve <- function(x, ...) {
  i <- x$getinverse()
  if(!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinverse(i)
  i
}
